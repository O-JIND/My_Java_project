package Ch01_variable_operator;

public class Trapezoid {
    public static void main(String[] args) {

        double a, b, h, d;

        a=10;
        b=20;
        h=15;
        d= (a+b)*h/2;

        System.out.println("밑변 : " + a);
        System.out.println("아랫변 : "  + b);
        System.out.println("높이 : " + h);
        System.out.println("면적 : " + d);







    }
}
